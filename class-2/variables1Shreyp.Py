# Variables are containers or storing data values
# A variable is created the moment you first assign a value to it.
# Varibles do not need to be declared with any particular type, and can even change type after they have been set.
x = 5        # Intiger 
y = "Jhon"   # String
# String variables can be declared either by using single or double quotes
z = 'Rudra'  # String
# Case sensitive 
X = "Sally"  # String
# You can get the data type of a variable with the type() function.
print(type(x), x)
print(type(z), z)

# If you want to secify the data type of a variable, this can be done with casting.
x = str(3)       # x will be '3'
