# Many Values to Multiple Variables
x, y, z = "Orange", "Bannana", "Cherry"
print(x)
print(y)
print(z)
# One Value to Multiple Variables
x = y = z = "Orange"
print(x)
print(y)
print(z)

# Unpack a Collection
fruits = ["apple", "bannana", "cherry"]
x, y, z = fruits
print(x)
print(y)
print(z)

